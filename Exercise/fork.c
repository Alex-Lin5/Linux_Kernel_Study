#include <stdio.h>

int main(){
  printf("1, %d\n", getpid());
  int p1 = fork();
  printf("2, p1=%d, %d\n", p1, getpid());
  int p2 = fork();
  printf("3, p2=%d, %d\n", p2, getpid());
  return 0;  
}
